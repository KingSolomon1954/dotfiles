export LESS_TERMCAP_mb=$'\e[5;32m'   # start blink green
export LESS_TERMCAP_md=$'\e[1;36m'   # start bold cyan
export LESS_TERMCAP_us=$'\e[4;32m'   # start underline green
export LESS_TERMCAP_ue=$'\e[0m'      # stop underline
export LESS_TERMCAP_so=$'\e[7m'      # start reverse video (stand out) yellow
export LESS_TERMCAP_se=$'\e[27m'     # stop reverse video
export LESS_TERMCAP_me=$'\e[0m'      # reset blink, bold, underline
